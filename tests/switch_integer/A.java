package switch_integer;

public class A {
  public static void main(String[] args) {
    Integer result = 0;
    switch (result) {
      case 0:
        System.out.println("0");
        break;
      case 1:
        System.out.println("1");
        break;
      default:
        System.out.println("None");
        break;
    }
  }
}
